package network.bisq.mobile.domain.data.replicated.account


data class ZelleAccountPayloadVO(
    override val id: String,
    override val paymentMethodName: String,
    val emailOrMobileNr: String,
    val holderName: String
) : AccountPayloadVO(id, paymentMethodName) {

    override fun verify() {
        require(emailOrMobileNr.isNotBlank()) { "Email or Mobile Number cannot be blank" }
        require(holderName.isNotBlank()) { "Holder Name cannot be blank" }
    }
}
